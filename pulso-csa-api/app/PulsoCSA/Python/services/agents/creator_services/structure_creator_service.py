#━━━━━━━━━❮Helpers – init & stub❯━━━━━━━━━
#━━━━━━━━━━━━━━❮◆❯━━━━━━━━━━━━━━


def _ensure_init(path: Path, created: List[str]) -> None:
    init_file = path / "__init__.py"
    if not init_file.exists():
        init_file.write_text("# Auto-generated by structural workflow\n", encoding="utf-8")
        created.append(str(init_file))


def _build_file_stub(item: PlannedFileCreation, request_id: str) -> str:
    header = f'"""Auto-generated file for {item.path} (request {request_id})."""\n\n'
    deps = f"# dependencies: {', '.join(item.dependencias)}\n\n" if item.dependencias else ""
    sections = [
        f"# section: {sec.name} -> {sec.description}" if isinstance(sec, PlannedFileSection)
        else f"# section: {sec.get('name')} -> {sec.get('description')}"
        for sec in item.secoes
    ]
    return header + deps + "\n".join(sections) + ("\n" if sections else "")


#━━━━━━━━━━━━━━❮◆❯━━━━━━━━━━━━━━
#━━━━━━━━━❮Função Principal – aplicar plano❯━━━━━━━━━
#━━━━━━━━━━━━━━❮◆❯━━━━━━━━━━━━━━


def apply_change_plan_to_filesystem(
    root_path: str,
    id_requisicao: str,
    novos_arquivos: List[PlannedFileCreation],
) -> Dict[str, Any]:
    try:
        project_root = Path(root_path)
        created_files: List[str] = []
        skipped: List[str] = []
        touched: set[str] = set()

        add_log("info", f"Applying dynamic plan for {id_requisicao}", "structure_creator")

        for item in novos_arquivos:
            final_path = _materialize_dynamic_path(project_root, item.path)
            final_folder = final_path.parent

            final_folder.mkdir(parents=True, exist_ok=True)
            touched.add(str(final_folder))

            _ensure_init(final_folder, created_files)

            if final_path.exists():
                skipped.append(str(final_path))
                continue

            content = _build_file_stub(item, id_requisicao)
            final_path.write_text(content, encoding="utf-8")
            created_files.append(str(final_path))

        add_log(
            "info",
            f"Dynamic creation done. Files={len(created_files)} skipped={len(skipped)}",
            "structure_creator",
        )

        return {
            "root_path": root_path,
            "created_files": created_files,
            "skipped_existing_files": skipped,
            "touched_dirs": list(touched),
            "message": "Dynamic structure applied successfully.",
        }
    except Exception as e:  # pragma: no cover – fallback
        log_workflow(id_requisicao, f"Erro ao criar estrutura: {e}")
        from utils.path_validation import is_production
        err_msg = "Erro ao criar estrutura." if is_production() else str(e)
        return {"erro": err_msg, "status": "falha"}


