def _infer_folder_pattern(directory: Path) -> str:
    """Detecta se o diretório é flat ou module-style."""
    if not directory.exists():
        return "module"

    subfolders = [f for f in directory.iterdir() if f.is_dir()]
    python_files = [f for f in directory.iterdir() if f.is_file() and f.suffix == ".py"]

    for sf in subfolders:
        if (sf / f"{sf.name}.py").exists():
            return "module"

    if python_files and not subfolders:
        return "flat"

    return "module"


#━━━━━━━━━━━━━━❮◆❯━━━━━━━━━━━━━━
#━━━━━━━━━❮Materialização de Caminho (evita duplicação)❯━━━━━━━━━
#━━━━━━━━━━━━━━❮◆❯━━━━━━━━━━━━━━


def _materialize_dynamic_path(project_root: Path, relative_path: str) -> Path:
    """
    Evita gerar dois routers:
      • se o caminho já contém '/routers/' OU termina com '_router.py',
        mantemos exatamente o path fornecido.
    Para os demais papéis continua a lógica anterior.
    """
    rel = Path(relative_path.lstrip("/\\"))
    if "/routers/" in rel.as_posix() or rel.name.endswith("_router.py"):
        # ❶  regra nova – nunca duplica routers
        return project_root / rel

    parent_dir = project_root / rel.parent
    stem = rel.stem

    if rel.parent.name == stem or rel.suffix != ".py":
        return project_root / rel

    pattern = _infer_folder_pattern(parent_dir)

    flat_candidate = parent_dir / f"{stem}.py"
    if pattern == "module" and not flat_candidate.exists():
        return parent_dir / stem / rel.name
    if pattern == "flat":
        return parent_dir / stem / rel.name

    return project_root / rel


