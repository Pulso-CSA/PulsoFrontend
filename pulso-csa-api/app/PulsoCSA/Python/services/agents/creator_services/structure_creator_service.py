#━━━━━━━━━❮Serviço – Estrutura a partir de relatório❯━━━━━━━━━
#━━━━━━━━━━━━━━❮◆❯━━━━━━━━━━━━━━


def generate_structure_from_report(root_path: str, id_requisicao: str):
    try:
        if not root_path or not str(root_path).strip():
            raise ValueError("root_path é obrigatório para criar estrutura e relatórios no disco.")
        root_path = os.path.normpath(os.path.abspath(str(root_path).strip()))
        reports_dir = os.path.join(root_path, "reports", id_requisicao)
        structure_report_path = os.path.join(reports_dir, "01_structure_report.json")
        if not os.path.exists(structure_report_path):
            raise FileNotFoundError(f"Relatório não encontrado: {structure_report_path}")

        with open(structure_report_path, encoding="utf-8") as f:
            data = json.load(f)

        estrutura = data.get("estrutura_arquivos", {})
        if not estrutura:
            raise ValueError("Relatório de estrutura inválido ou vazio.")

        # Garantir estrutura completa para API (auth_router, auth_service, user_model, settings)
        summary_path = os.path.join(reports_dir, "summary_pipeline.json")
        if os.path.exists(summary_path):
            try:
                with open(summary_path, encoding="utf-8") as sf:
                    summary = json.load(sf)
                refined = (summary.get("refined_prompt") or "").strip()
                if refined:
                    from services.agents.analise_services.structure_service import _ensure_complete_api_structure, _is_api_project
                    if _is_api_project(refined):
                        estrutura = _ensure_complete_api_structure(estrutura, refined)
            except Exception:
                pass

        # --- normalização rápida ---
        normalized: Dict[str, list[str]] = {}
        root_files: list[str] = []
        for key, value in estrutura.items():
            cleaned_key = key.strip().rstrip("/\\")
            if os.path.splitext(cleaned_key)[1] and (not value or not isinstance(value, list)):
                root_files.append(cleaned_key)
            elif os.path.splitext(cleaned_key)[1] and isinstance(value, list) and not value:
                root_files.append(cleaned_key)
            else:
                normalized[cleaned_key] = value
        if root_files:
            normalized[""] = root_files
        estrutura = normalized

        base_dir = os.path.join(root_path, id_requisicao, "generated_code")
        base_dir_abs = os.path.abspath(base_dir)
        os.makedirs(base_dir, exist_ok=True)

        created: Dict[str, list[str]] = {}
        skipped: list[str] = []
        special_files = {".env", "Dockerfile", "Makefile", "docker-compose.yml", "README", "README.md"}

        for folder, files in estrutura.items():
            safe_folder = sanitize_relative_path(str(folder or ".").strip())
            if safe_folder is None:
                add_log("warning", f"structure_creator: folder ignorado (path traversal): {folder}", "structure_creator")
                continue
            folder_path = base_dir if safe_folder == "." else os.path.join(base_dir, safe_folder)
            if not is_path_under_base(os.path.abspath(folder_path), base_dir_abs):
                add_log("warning", f"structure_creator: path fora da base ignorado: {folder}", "structure_creator")
                continue
            if folder and not os.path.exists(folder_path):
                os.makedirs(folder_path, exist_ok=True)

            created[folder or "."] = []
            has_py = any(isinstance(f, str) and f.endswith(".py") for f in files)
            if has_py and folder and "__init__.py" not in files:
                init_path = os.path.join(folder_path, "__init__.py")
                if is_path_under_base(os.path.abspath(init_path), base_dir_abs):
                    with open(init_path, "w", encoding="utf-8") as f:
                        f.write('"""Inicialização do pacote."""\n')
                    created[folder or "."].append("__init__.py")
            for file in files:
                if not isinstance(file, str) or not file or ".." in file or file.startswith("/") or file.startswith("\\"):
                    add_log("warning", f"structure_creator: arquivo ignorado (inseguro): {file}", "structure_creator")
                    continue
                file_path = os.path.join(folder_path, file)
                if not is_path_under_base(os.path.abspath(file_path), base_dir_abs):
                    add_log("warning", f"structure_creator: path fora da base ignorado: {folder}/{file}", "structure_creator")
                    continue
                name, ext = os.path.splitext(file)
                is_file = bool(ext) or file in special_files

                if is_file:
                    if os.path.isdir(file_path):
                        shutil.rmtree(file_path, ignore_errors=True)
                    with open(file_path, "w", encoding="utf-8") as f:
                        f.write("# Arquivo gerado automaticamente\n")
                    created[folder or "."].append(file)
                else:
                    os.makedirs(file_path, exist_ok=True)

        manifest = {
            "id_requisicao": id_requisicao,
            "root_path": base_dir,
            "created": created,
            "skipped": skipped,
            "timestamp": datetime.now().isoformat(),
            "status": "sucesso",
        }

        with open(os.path.join(base_dir, "structure_manifest.json"), "w", encoding="utf-8") as f:
            json.dump(manifest, f, indent=4, ensure_ascii=False)

        db_c3.upsert_execution_manifest(id_requisicao, manifest)
        log_workflow(id_requisicao, f"Estrutura criada com sucesso em {base_dir}")
        return manifest

    except Exception as e:  # pragma: no cover
        log_workflow(id_requisicao, f"Erro ao criar estrutura: {e}")
        from utils.path_validation import is_production
        err_msg = "Erro ao criar estrutura." if is_production() else str(e)
        return {"erro": err_msg, "status": "falha"}
